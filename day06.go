func intersectBytes(bytesA []byte, bytesB []byte) []byte {
	res := []byte{}
	for _, a := range bytesA {
		if bytes.Contains(bytesB, []byte{a}) {
			res = append(res, a)
		}
	}
	// fmt.Printf("intersect(%s, %s) = %s\n", string(bytesA), string(bytesB), string(res))
	return res
}

func intersectStrings(strA string, strB string) string {
	return string(intersectBytes([]byte(strA), []byte(strB)))
}

func countAllYesAnswers(forms []string) int {
	commonAnswer := []byte(forms[0])
	for i := 1; i < len(forms); i++ {
		commonAnswer = intersectBytes(commonAnswer, []byte(forms[i]))
	}
	return len(commonAnswer)
}

