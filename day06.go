func getYesAnswers(form string) int {
	answers := [26]bool{}
	for i := 0; i < 26; i++ {
		answers[i] = false
	}
	for i := 0; i < len(form); i++ {
		answers[form[i]-'a'] = true
	}
	count := 0
	for i := 0; i < len(answers); i++ {
		if answers[i] {
			count++
		}
	}
	return count
}

func countYesAnswers(forms []string) {
	count := 0
	for _, form := range forms {
		count += getYesAnswers(form)
	}
	fmt.Printf("You have a total of %d Yes answers\n", count)
}

